    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        if(textField.getText().isBlank()){
            JLabel message = new JLabel("Insira um nome antes de salvar!");
            message.setFont(new Font("Arial",Font.BOLD,48));
            JOptionPane.showMessageDialog(null, message);
        }
        else{
            String name = textField.getText().trim();
            playerIds.add(name);
            
            switch (playerIds.size()){
                case 1:
                    player1.setText(playerIds.get(0));
                break;
                case 2:
                    player1.setText(playerIds.get(0));
                    player2.setText(playerIds.get(1));
                break;
                case 3:
                    player1.setText(playerIds.get(0));
                    player2.setText(playerIds.get(1));
                    player3.setText(playerIds.get(2));
                break;
                case 4:
                    player1.setText(playerIds.get(0));
                    player2.setText(playerIds.get(1));
                    player3.setText(playerIds.get(2));
                    player4.setText(playerIds.get(3));
                break;
            }
        }
        
        if(playerIds.size() == 5){
            playerIds.remove(4);
            JLabel message = new JLabel("SÃ³ pode haver 2-4 jogadores!");
            JOptionPane.showMessageDialog(null, message);
            textField.setText("");
        }

        if(playerIds.size() > 0 && playerIds.size() < 5){
            JLabel message = new JLabel("Jogador Salvo!");
            JOptionPane.showMessageDialog(null, message);
            textField.setText("");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void doneBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneBtnActionPerformed
        if(playerIds.size() <= 1){
            JLabel message = new JLabel("Deve haver 2-4 jogadores!");
            JOptionPane.showMessageDialog(null, message);
        }
        else{
            this.dispose();
            new GameStage(playerIds).setVisible(true);
        }
    }//GEN-LAST:event_doneBtnActionPerformed

